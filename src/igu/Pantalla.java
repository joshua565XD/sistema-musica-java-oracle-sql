    private void playButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playButtonActionPerformed
    if (filePath == null || filePath.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, seleccione un archivo primero.");
        return;
    }
    PanelReproductor musica = new PanelReproductor();
    musica.play(filePath);
    
    pantallaRep.removeAll(); // Limpiar cualquier componente anterior en el panel
    pantallaRep.add(musica); // AÃ±adir el panel reproductor al panel de la pantalla
    pantallaRep.revalidate(); // Actualizar el panel
    pantallaRep.repaint(); // Repintar el panel
    pantallaRep.setVisible(true);
    }//GEN-LAST:event_playButtonActionPerformed

    private void agregarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarButtonActionPerformed
JFileChooser fileChooser = new JFileChooser();
    fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("MP3 Files", "mp3"));
    
    int returnValue = fileChooser.showOpenDialog(null);
    if (returnValue == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();
        filePath = selectedFile.getAbsolutePath();
        System.out.println("Archivo seleccionado: " + filePath); // Para depuraciÃ³n
    }      // TODO add your handling code here:
    }//GEN-LAST:event_agregarButtonActionPerformed

